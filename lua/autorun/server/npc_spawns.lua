local function CreateEnt( class, pos, ang, data )
	local e = ents.Create( class )
	e:SetPos( pos )
	e:SetAngles( ang )
	e:Spawn()
	e:Activate()
	if isstring( data ) then
		e:SetModel( data )
	elseif isnumber( data ) then
		e:ApplyType( data )
	end
	return e
end

